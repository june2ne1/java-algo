public class DoWhileLoopTen {
	public static void main(String[] args) {
		
		int i = 0;
		
		do {
			i++;
			System.out.print(i+"\t");
				
		}while (i<10);
		for (int j = 0; j < 10; j++) {
			System.out.println(j+1);
			
		}
		
		
	}
}